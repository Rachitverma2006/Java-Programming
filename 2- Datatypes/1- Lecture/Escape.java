public class Escape {
    public static void main(String[] args) {
        // Escape Sequency :- Suppose you want to escape the functioning of " than place \ before it.
        // \n - for next line character.
        // \b - for back space character.
        // \t - for tab space.
        System.out.println("Hello \"World\"");
        System.out.println("Hello \\World");
        System.out.println("Hello \nWorld");
    }
}
