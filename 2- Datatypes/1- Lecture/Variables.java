public class Variables {
    public static void main(String[] args) {

        // in naming convention
        // 1- only 2 special symbols are allowed $ and _
        // 2- variable name should not start with number
        // 3- only [A-Z] , [a-z] and [0-9] are allowed
        int myNumber = 865;

        int yourNumber;
        yourNumber = 800;
        System.out.println(yourNumber);
        yourNumber = 545;
        System.out.println(yourNumber);

        boolean myBoolean = true;
        System.out.println(myBoolean);

        float smallNumber = 5.0f;
        double y = 5.898654;
        System.out.println(smallNumber);

        String wishes = "Good Night";
        System.out.println(wishes);

        char myChar = 'A';
        System.out.println(myChar);

        // Numbers are called literals.

    }
}
